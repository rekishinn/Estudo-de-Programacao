public class instrução_arithmetic_operators_exemplo001 {
    public static void main(String[] args) {

        int valor1, valor2;

        valor1 = 10;
        valor2 = 5;

        System.out.println(valor1==valor2);
        System.out.println(valor1!=valor2);
        System.out.println(valor1<=valor2);
        System.out.println(valor1>=valor2);
        System.out.println(valor1<valor2);
        System.out.println(valor1>valor2);

    }
}
